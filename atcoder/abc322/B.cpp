ll solve() {
    int n, m; cin >> n >> m;
    string s, t; cin >> s >> t;
    bool prefix = 1, suffix = 1;
    for (int i = 0; i < n; i++) {
        prefix &= s[i] == t[i];
    }
    for (int i = 0; i < n; i++) {
        suffix &= s[i] == t[m - n + i];
    }
    if (prefix && suffix) {
        return 0;
    } else if (prefix) return 1;
    else if (suffix) return 2;
    return 3;
}

