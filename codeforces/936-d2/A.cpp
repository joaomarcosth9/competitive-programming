void solve() {
    int n; cin >> n;
    vector<int> a(n + 1);
    for (int i = 1; i <= n; i++) cin >> a[i];
    sort(a.begin() + 1, a.end());
    map<int, int> f;
    for (int i = (n + 1) / 2; i <= n; i++) {
        f[a[i]]++;
    }
    cout << (f[a[(n + 1) / 2]]) << endl;
}

