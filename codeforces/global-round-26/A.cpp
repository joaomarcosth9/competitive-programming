void solve() {
    int n; cin >> n;
    vector<int> a(n);
    for (int i = 0; i < n; i++) {
        cin >> a[i];
    }
    if (a[0] == a[n - 1]) {
        cout << "NO" << endl;
    } else {
        cout << "YES" << endl;
        cout << "BR";
        for (int i = 2; i < n; i++) {
            cout << "B";
        }
        cout << endl;
    }
}

